public class FirstProgram { // Writing the Public here doesn't affect the program
    public static void main(String[] args) { // static means this method can be called without creating an instance of the class, void means this method doesn't return a value, string [] args means this method can accept an array of strings as arguments
        System.out.println("Hello, World!"); // System.out.println is a method that prints the string inside the parentheses to the console
    }
}
